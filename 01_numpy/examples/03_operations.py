"""
NumPy 기초 - 연산
=================
요소별 연산, 브로드캐스팅, 행렬 연산, 통계 함수를 익힌다.
이 연산들이 ML에서 가설 계산, 손실 계산 등에 직접 사용된다.
"""

import numpy as np

# ============================================================
# 1. 요소별(Element-wise) 연산
#    같은 shape의 배열끼리 +, -, *, / 하면 요소별로 계산된다.
# ============================================================
a = np.array([1, 2, 3, 4])
b = np.array([10, 20, 30, 40])

print("=== 요소별 연산 ===")
print(f"a = {a}")
print(f"b = {b}")
print(f"a + b = {a + b}")   # [11, 22, 33, 44]
print(f"a * b = {a * b}")   # [10, 40, 90, 160]
print(f"a ** 2 = {a ** 2}") # [1, 4, 9, 16]

# ============================================================
# 2. 브로드캐스팅 (Broadcasting)
#    shape이 다른 배열 간의 연산을 자동으로 확장해주는 기능이다.
#    스칼라, 1D, 2D 간 연산을 for문 없이 처리할 수 있다.
# ============================================================
print("\n=== 브로드캐스팅 ===")

# 스칼라와 배열: 스칼라가 배열 크기로 확장
arr = np.array([1, 2, 3])
print(f"arr * 3 = {arr * 3}")         # [3, 6, 9]
print(f"arr + 10 = {arr + 10}")       # [11, 12, 13]

# 2D + 1D: 1D 배열이 각 행에 대해 반복 적용
matrix = np.array([[1, 2, 3],
                   [4, 5, 6]])
row = np.array([10, 20, 30])
print(f"\nmatrix:\n{matrix}")
print(f"row:    {row}")
print(f"matrix + row:\n{matrix + row}")
# [[11, 22, 33],
#  [14, 25, 36]]

# ============================================================
# 3. 내적 (Dot Product)
#    ML에서 가설 H(x) = w·x 계산에 핵심적으로 쓰인다.
# ============================================================
print("\n=== 내적 ===")
w = np.array([0.5, 0.3])
x = np.array([4.0, 2.0])
result = np.dot(w, x)     # 0.5*4 + 0.3*2 = 2.6
print(f"w = {w}")
print(f"x = {x}")
print(f"np.dot(w, x) = {result}")

# 행렬 × 벡터
W = np.array([[1, 2],
              [3, 4]])
v = np.array([5, 6])
print(f"\nW @ v = {W @ v}")   # [1*5+2*6, 3*5+4*6] = [17, 39]

# ============================================================
# 4. 통계 함수
#    합계, 평균, 최대/최소 등을 한 줄로 구한다.
#    손실 함수에서 평균 계산 시 자주 사용된다.
# ============================================================
print("\n=== 통계 함수 ===")
data = np.array([10, 25, 30, 45, 50])
print(f"data = {data}")
print(f"합계: {np.sum(data)}")       # 160
print(f"평균: {np.mean(data)}")      # 32.0
print(f"최대: {np.max(data)}")       # 50
print(f"최소: {np.min(data)}")       # 10
print(f"표준편차: {np.std(data):.2f}")  # 14.14

# 2D 배열에서 축(axis) 지정
matrix = np.array([[1, 2, 3],
                   [4, 5, 6]])
print(f"\nmatrix:\n{matrix}")
print(f"전체 합: {np.sum(matrix)}")          # 21
print(f"열별 합 (axis=0): {np.sum(matrix, axis=0)}")  # [5, 7, 9]
print(f"행별 합 (axis=1): {np.sum(matrix, axis=1)}")  # [6, 15]

# ============================================================
# 5. 비교 연산과 활용
#    배열 간 비교는 True/False 배열을 반환한다.
# ============================================================
print("\n=== 비교 연산 ===")
predictions = np.array([2.5, 3.1, 3.4, 4.2])
labels = np.array([2.5, 3.0, 3.5, 4.0])
errors = predictions - labels
print(f"예측값: {predictions}")
print(f"실제값: {labels}")
print(f"오차:   {errors}")
print(f"MSE:    {np.mean(errors ** 2):.4f}")
