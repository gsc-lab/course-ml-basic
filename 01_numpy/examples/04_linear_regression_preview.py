"""
NumPy 활용 - 선형회귀 맛보기
=============================
for문으로 작성한 선형회귀 코드를 numpy로 변환하면 어떻게 바뀌는지 비교한다.
"numpy를 왜 배우는가?"에 대한 답을 직접 확인한다.
"""

import numpy as np

# ============================================================
# 데이터셋: H(x) = 0.5x + 2  →  w=0.5, b=2
# ============================================================
x_data = [1, 2, 3, 4]
y_data = [2.5, 3.0, 3.5, 4.0]

# numpy 배열로도 준비
x_np = np.array(x_data, dtype=np.float64)
y_np = np.array(y_data, dtype=np.float64)


# ============================================================
# 방법 1: for문 버전 (02장에서 배운 방식)
# ============================================================
print("=" * 50)
print("방법 1: for문 버전")
print("=" * 50)

w, b = 0.0, 0.0
learning_rate = 0.01
n = len(x_data)

for epoch in range(2000):
    # 가설
    y_pred = []
    for x in x_data:
        y_pred.append(w * x + b)

    # 손실
    loss = 0.0
    for i in range(n):
        loss += (y_pred[i] - y_data[i]) ** 2
    loss = loss / n

    # 기울기
    grad_w = 0.0
    grad_b = 0.0
    for i in range(n):
        error = y_pred[i] - y_data[i]
        grad_w += error * x_data[i]
        grad_b += error
    grad_w = (2 / n) * grad_w
    grad_b = (2 / n) * grad_b

    # 업데이트
    w = w - learning_rate * grad_w
    b = b - learning_rate * grad_b

print(f"w = {w:.4f}, b = {b:.4f}")


# ============================================================
# 방법 2: numpy 버전
#   - for문 없이 배열 연산만으로 동일한 결과를 얻는다.
#   - 코드가 짧고, 대규모 데이터에서 훨씬 빠르다.
# ============================================================
print(f"\n{'=' * 50}")
print("방법 2: numpy 버전")
print("=" * 50)

w, b = 0.0, 0.0

for epoch in range(2000):
    # 가설: for문 4줄 → numpy 1줄
    y_pred = w * x_np + b

    # 손실: for문 4줄 → numpy 1줄
    loss = np.mean((y_pred - y_np) ** 2)

    # 기울기: for문 6줄 → numpy 2줄
    error = y_pred - y_np
    grad_w = (2 / n) * np.sum(error * x_np)
    grad_b = (2 / n) * np.sum(error)

    # 업데이트 (동일)
    w = w - learning_rate * grad_w
    b = b - learning_rate * grad_b

print(f"w = {w:.4f}, b = {b:.4f}")


