"""
Stochastic Gradient Descent (SGD) - 확률적 경사하강법
=====================================================
BGD와 동일한 데이터·동일한 초기값으로 학습하되,
업데이트 방식만 SGD로 바꿔서 차이를 관찰한다.

핵심 개념:
  SGD는 데이터를 **1개씩** 볼 때마다 즉시 파라미터를 업데이트한다.

  한 epoch의 흐름:
    샘플 1개 → 기울기 계산 → 즉시 업데이트 → 다음 샘플 → ...

  장점: 업데이트가 빈번하여 빠르게 수렴할 수 있다.
        데이터가 아무리 많아도 1개만 보고 바로 업데이트한다.
  단점: 샘플 하나의 기울기만 사용하므로 방향이 불안정(진동)하다.
        매 epoch마다 데이터 순서를 섞어줘야(shuffle) 편향을 줄일 수 있다.

BGD와의 비교:
  - BGD: 20개 데이터 → 기울기 평균 → 1번 업데이트  (epoch당 1회)
  - SGD: 20개 데이터 → 1개마다 업데이트            (epoch당 20회)

특징 정리:
  - 매 샘플마다 즉시 업데이트 → epoch당 n번 업데이트
  - 업데이트가 빈번하여 빠르게 움직이지만, 방향이 불안정(진동)
  - 데이터가 수백만 개여도 1개씩 업데이트하므로 메모리 효율적
  - 매 epoch마다 shuffle하여 학습 순서의 편향을 방지
"""
import random

# ============================================================
# 1. 데이터셋 (BGD 예제와 동일)
#    정답: H(x) = 0.5x + 2  →  w=0.5, b=2
# ============================================================
random.seed(0)
x_data = [i for i in range(1, 21)]
y_data = [0.5 * x + 2 + random.uniform(-0.3, 0.3) for x in x_data]

# ============================================================
# 2. 하이퍼파라미터
#    SGD는 매번 즉시 업데이트하므로 learning_rate를 더 작게 설정한다.
#    너무 크면 진동이 심해져서 수렴하지 못한다.
# ============================================================
learning_rate = 0.0001
epochs = 300
n = len(x_data)

# ============================================================
# 3. 파라미터 초기화 (BGD 예제와 동일한 초기값)
# ============================================================
random.seed(42)
w = random.random()
b = random.random()

print("-" * 10)
print("Stochastic Gradient Descent (SGD)")
print("-" * 10)
print(f"데이터 수: {n}개")
print(f"초기 w: {w:.4f}, 초기 b: {b:.4f}")
print()

# ============================================================
# 4. 학습 루프 (SGD)
#    핵심: 샘플 1개를 볼 때마다 즉시 업데이트한다.
#          매 epoch마다 데이터 순서를 섞어(shuffle) 편향을 줄인다.
# ============================================================
for epoch in range(1, epochs + 1):
    # --- 매 epoch마다 데이터 순서를 섞는다 (shuffle) ---
    # 같은 순서로 반복하면 특정 패턴에 편향될 수 있기 때문이다.
    indices = list(range(n))
    random.shuffle(indices)

    epoch_loss = 0.0

    for idx in indices:
        x = x_data[idx]
        y = y_data[idx]

        # 예측
        predict = w * x + b
        error = predict - y

        # 기울기 계산 (샘플 1개에 대해)
        w_grad = 2 * x * error
        b_grad = 2 * error

        # --- 즉시 업데이트 (BGD와의 핵심 차이) ---
        w = w - learning_rate * w_grad
        b = b - learning_rate * b_grad

        epoch_loss += error ** 2

    epoch_loss /= n

    if epoch % 50 == 0:
        print(f"Epoch {epoch:4d} | Loss: {epoch_loss:.6f} | w: {w:.4f}, b: {b:.4f}")

# ============================================================
# 5. 결과
# ============================================================
print()
print("-" * 10)
print("학습 완료")
print(f"  학습된 w: {w:.4f}  (정답: 0.5)")
print(f"  학습된 b: {b:.4f}  (정답: 2.0)")
