"""
Batch Gradient Descent (BGD) - 배치 경사하강법
==============================================
01번 예제의 경사하강법을 "BGD"라는 이름으로 정리하고,
왜 전체 데이터를 한 번에 사용하는지 그 의미를 이해한다.

핵심 개념:
  BGD는 매 업데이트마다 **전체 데이터셋**의 기울기 평균을 구해서
  파라미터를 수정한다.

  한 epoch의 흐름:
    전체 데이터 순회 → 기울기 누적 → 평균 → 1번 업데이트

  장점: 전체 데이터를 보고 업데이트하므로 방향이 안정적이다.
  단점: 데이터가 많으면 1번 업데이트에 시간이 오래 걸린다.

특징 정리:
  - 매 epoch마다 전체 데이터(n개)를 순회한 뒤 1번 업데이트
  - 기울기가 전체 데이터의 평균이므로 업데이트 방향이 안정적
  - 데이터가 수만~수백만 개라면 1번 업데이트가 매우 느려진다
"""
import random

# ============================================================
# 1. 데이터셋 (20개 - BGD vs SGD 차이를 관찰하기 위해 확장)
#    정답: H(x) = 0.5x + 2  →  w=0.5, b=2
# ============================================================
random.seed(0)
x_data = [i for i in range(1, 21)]
y_data = [0.5 * x + 2 + random.uniform(-0.3, 0.3) for x in x_data]

# ============================================================
# 2. 하이퍼파라미터
# ============================================================
learning_rate = 0.001
epochs = 300
n = len(x_data)

# ============================================================
# 3. 파라미터 초기화
# ============================================================
random.seed(42)
w = random.random()
b = random.random()

print("-" * 10)
print("Batch Gradient Descent (BGD)")
print("-" * 10)
print(f"데이터 수: {n}개")
print(f"초기 w: {w:.4f}, 초기 b: {b:.4f}")
print()

# ============================================================
# 4. 학습 루프 (BGD)
#    핵심: 전체 데이터를 한 바퀴 돌면서 기울기를 "누적"한 뒤,
#          평균을 내서 "1번만" 업데이트한다.
# ============================================================
for epoch in range(1, epochs + 1):
    # --- 전체 데이터에 대한 기울기 누적 ---
    w_grad = 0.0
    b_grad = 0.0
    loss = 0.0

    for x, y in zip(x_data, y_data):
        predict = w * x + b        # 가설
        error = predict - y        # 오차

        w_grad += 2 * x * error    # ∂Loss/∂w 누적
        b_grad += 2 * error        # ∂Loss/∂b 누적
        loss += error ** 2         # 손실 누적

    # --- 평균 ---
    w_grad /= n
    b_grad /= n
    loss /= n

    # --- 1번 업데이트 (epoch당 1회) ---
    w = w - learning_rate * w_grad
    b = b - learning_rate * b_grad

    if epoch % 50 == 0:
        print(f"Epoch {epoch:4d} | Loss: {loss:.6f} | w: {w:.4f}, b: {b:.4f}")

# ============================================================
# 5. 결과
# ============================================================
print()
print("-" * 10)
print("학습 완료")
print(f"  학습된 w: {w:.4f}  (정답: 0.5)")
print(f"  학습된 b: {b:.4f}  (정답: 2.0)")
