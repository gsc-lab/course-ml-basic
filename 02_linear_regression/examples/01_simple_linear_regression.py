"""
Linear Regression - 기본 예제
순수 Python(for문)으로 구현하여 가설, 손실함수, 옵티마이저의 관계를 이해한다.
"""

# ============================================================
# 1. 데이터셋
#    정답: H(x) = 0.5x + 2  →  w=0.5, b=2
# ============================================================
x_data = [1, 2, 3, 4]
y_data = [2.5, 3.0, 3.5, 4.0]

# ============================================================
# 2. 학습할 파라미터 초기화 (임의의 값으로 시작)
# ============================================================
w = 0.0
b = 0.0

# ============================================================
# 3. 하이퍼파라미터
# ============================================================
learning_rate = 0.01
epochs = 2000
n = len(x_data)

# ============================================================
# 4. 학습 루프
# ============================================================
for epoch in range(epochs):

    # ----------------------------------------------------------
    # (A) 가설 (Hypothesis): H(x) = wx + b
    #     모델이 현재 w, b로 예측한 값을 구한다.
    # ----------------------------------------------------------
    y_pred = []
    for x in x_data:
        y_pred.append(w * x + b)

    # ----------------------------------------------------------
    # (B) 손실 함수 (Loss Function): MSE = (1/n) Σ (H(x) - y)²
    #     예측값과 실제값의 차이를 하나의 숫자로 요약한다.
    #     이 값이 작을수록 모델이 데이터를 잘 설명한다.
    # ----------------------------------------------------------
    loss = 0.0
    for i in range(n):
        loss += (y_pred[i] - y_data[i]) ** 2
    loss = loss / n

    # ----------------------------------------------------------
    # (C) 옵티마이저 (Optimizer): Gradient Descent
    #     손실을 줄이는 방향으로 w, b를 업데이트한다.
    #
    #     ∂Loss/∂w = (2/n) Σ (H(x) - y) · x
    #     ∂Loss/∂b = (2/n) Σ (H(x) - y)
    #
    #     관계 정리:
    #       가설 → 예측값 생성
    #       손실함수 → 예측이 얼마나 틀렸는지 측정
    #       옵티마이저 → 손실의 기울기(gradient)를 이용해 파라미터 수정
    # ----------------------------------------------------------
    grad_w = 0.0
    grad_b = 0.0
    for i in range(n):
        error = y_pred[i] - y_data[i]
        grad_w += error * x_data[i]
        grad_b += error
    grad_w = (2 / n) * grad_w
    grad_b = (2 / n) * grad_b

    # 파라미터 업데이트: w ← w - lr * ∂Loss/∂w
    w = w - learning_rate * grad_w
    b = b - learning_rate * grad_b

    # ----------------------------------------------------------
    # 학습 경과 출력
    # ----------------------------------------------------------
    if epoch % 200 == 0:
        print(f"Epoch {epoch:4d} | Loss: {loss:.6f} | w: {w:.4f}, b: {b:.4f}")

# ============================================================
# 5. 학습 결과
# ============================================================
print("\n========== 학습 완료 ==========")
print(f"학습된 w: {w:.4f}  (정답: 0.5)")
print(f"학습된 b: {b:.4f}  (정답: 2.0)")
print()
print("예측 결과:")
for x, y in zip(x_data, y_data):
    pred = w * x + b
    print(f"  x={x} → 예측: {pred:.4f}, 정답: {y}")
