"""
Linear Regression - 기본 예제
순수 Python(for문)으로 구현하여 가설, 손실함수, 옵티마이저의 관계를 이해한다.

핵심 개념:
  가설(Hypothesis):  H(x) = w * x + b
  손실함수(Loss):    MSE = (1/n) Σ (H(x) - y)²
  옵티마이저:        Gradient Descent
    ∂Loss/∂w = (2/n) Σ (H(x) - y) · x
    ∂Loss/∂b = (2/n) Σ (H(x) - y)

  가설 → 예측값 생성
  손실함수 → 예측이 얼마나 틀렸는지 측정
  옵티마이저 → 손실의 기울기(gradient)를 이용해 파라미터 수정
"""
import random

# ============================================================
# 1. 데이터셋
#    정답: H(x) = 0.5x + 2  →  w=0.5, b=2
# ============================================================
x_data = [1, 2, 3, 4]
y_data = [2.5, 3.0, 3.5, 4.0]
n = len(x_data)

# ============================================================
# 2. 하이퍼파라미터
# ============================================================
learning_rate = 0.01
epochs = 2000

# ============================================================
# 3. 파라미터 초기화 (임의의 값으로 시작)
# ============================================================
random.seed(42)
w = random.random()
b = random.random()

# ============================================================
# 4. 학습 루프 (Batch Gradient Descent)
#    전체 데이터를 한 바퀴 돌면서 기울기를 누적한 뒤,
#    평균을 내서 1번 업데이트한다.
# ============================================================
for epoch in range(1, epochs + 1):
    w_grad = 0.0
    b_grad = 0.0
    loss = 0.0

    for x, y in zip(x_data, y_data):
        # 가설: H(x) = w * x + b
        predict = w * x + b

        # 오차: error = H(x) - y
        error = predict - y

        # 기울기 누적
        w_grad += 2 * x * error
        b_grad += 2 * error

        # 손실 누적
        loss += error ** 2

    # 평균
    w_grad /= n
    b_grad /= n
    loss /= n

    # 파라미터 업데이트: w ← w - lr * ∂Loss/∂w
    w = w - learning_rate * w_grad
    b = b - learning_rate * b_grad

    if epoch % 200 == 0:
        print(f"Epoch {epoch:4d} | Loss: {loss:.6f} | w: {w:.4f}, b: {b:.4f}")

# ============================================================
# 5. 학습 결과
# ============================================================
print("\n" + "-" * 10)
print(f"학습된 w: {w:.4f}  (정답: 0.5)")
print(f"학습된 b: {b:.4f}  (정답: 2.0)")
print()
print("예측 결과:")
for x, y in zip(x_data, y_data):
    pred = w * x + b
    print(f"  x={x} → 예측: {pred:.4f}, 정답: {y}")
